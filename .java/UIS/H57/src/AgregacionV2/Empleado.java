
package AgregacionV2;

public class Empleado {
    private String name;
    private String apellido;

    public Empleado(String name, String apellido) {
        this.name = name;
        this.apellido = apellido;
    }

    public String getName() {
        return name;
    }

    public String getApellido() {
        return apellido;
    }
    
    
}
