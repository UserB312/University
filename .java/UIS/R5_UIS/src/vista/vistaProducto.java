    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        try {
            Producto P = new Producto(
            txtNombre.getText()
            ,txtId.getText()
            ,Double.parseDouble(txtTemperatura.getText())
            ,Double.parseDouble(txtValorBase.getText())
            );
            if(P.agregarProducto()){
                JOptionPane.showMessageDialog(null, "Producto registrado exitosamente","", JOptionPane.INFORMATION_MESSAGE);
                mostrarProductos();
                limpiarVentana();
            }else{
                JOptionPane.showMessageDialog(null, "Error al registrar el producto", "¡ERROR!", JOptionPane.ERROR_MESSAGE);
            }
            
        } catch (Exception e){
            JOptionPane.showMessageDialog(null, "Todos los campos son obligatorios", "¡ERROR!", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnAgregarActionPerformed
    
    public void mostrarProductos(){
        DefaultTableModel modelo=(DefaultTableModel) jtUsuarios.getModel();
        Producto p=new Producto();
        modelo.setRowCount(0);
        List<Producto> listaProductos=p.listarProductos("");
        for (Producto pro:listaProductos) {
            Object vector[]={pro.getNombre(),pro.getId(),pro.getTemperatura(),pro.getValorBase()};
            modelo.addRow(vector);
        }
    }
    
    public void limpiarVentana(){
        btnActualizar.setEnabled(false);
        btnEliminar.setEnabled(false);
        btnCancelar.setEnabled(false);
        btnAgregar.setEnabled(true);
        btnConsultar.setEnabled(true);
        txtId.setEnabled(true);
        txtNombre.setText("");
        txtId.setText("");
        txtTemperatura.setText("");
        txtValorBase.setText("");  
    }
    
    public void editarVentana(){
        btnActualizar.setEnabled(true);
        btnEliminar.setEnabled(true);
        btnCancelar.setEnabled(true);
        btnAgregar.setEnabled(false);
        btnConsultar.setEnabled(true);
        txtId.setEnabled(false);
    }
    
                
