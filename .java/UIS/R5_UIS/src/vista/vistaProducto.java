    private void btnConsultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConsultarActionPerformed
        //CONSULTA MEDIANTE EL NOMBRE
        
        DefaultTableModel modelo=(DefaultTableModel) jtUsuarios.getModel();
        Producto p=new Producto();
        String filtro="";
        if(txtNombre.getText().length()>0){
            filtro=" where nombre like '%"+txtNombre.getText()+"%'";
        }
        
        modelo.setRowCount(0);
        List<Producto> listaProductos=p.listarProductos(filtro);
        for (Producto pro:listaProductos) {
            Object vector[]={pro.getNombre(),pro.getId(),pro.getTemperatura(),pro.getValorBase()};
            modelo.addRow(vector);
        }
    }//GEN-LAST:event_btnConsultarActionPerformed

